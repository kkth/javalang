public class MyType2 {
    private int intVal;
    private String strVal;
    private String dVal;

    public MyType2(int intVal, String strVal, String dVal) {
        this.intVal = intVal;
        this.strVal = strVal;
        this.dVal = dVal;
    }

    public int getIntVal() {
        return intVal;
    }

    public void setIntVal(int intVal) {
        this.intVal = intVal;
    }

    public String getStrVal() {
        return strVal;
    }

    public void setStrVal(String strVal) {
        this.strVal = strVal;
    }

    public String getdVal() {
        return dVal;
    }

    public void setdVal(String dVal) {
        this.dVal = dVal;
    }
}
